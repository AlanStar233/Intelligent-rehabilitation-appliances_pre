#include <ioCC2530.h>
#include "led.h"
void ledInit(void)
{
  P1DIR |= 0x01;     //P1_0定义为输出
  P1INP |= 0X01;     //打开下拉
  P1DIR |= 0x02;     //P1_1定义为输出
  P1INP |= 0X02;     //打开下拉

}

